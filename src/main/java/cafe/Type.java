package cafe;

public class Type {
	private long id;
	private long cafeId;
	private String name;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getCafeId() {
		return cafeId;
	}
	public void setCafeId(long cafeId) {
		this.cafeId = cafeId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
